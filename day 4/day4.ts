const SAMPLE_ANSWER = 13;


let accessibleRolls: number = 0;
const NUMBER_OF_ROLLS_ACCESSIBLE = 4;
const ROLL = '@';

export const buildRollsMatrix = (rollsInput: string[]): ("@" | ".")[][] => {

    const rollsMatrix: ("@" | "." )[][] = [];

    rollsInput.forEach((rowString) => {

        const rowArray: ("@" | ".")[] = [];

        for (let charIndex = 0; charIndex < rowString.length; charIndex++) {

            const char = rowString[charIndex];

            if (char === ROLL || char === ".") {
                rowArray.push(char);
            } 
        }

        rollsMatrix.push(rowArray);
    });

    return rollsMatrix;
}

export const calculateIfRollIsSafe = (rolls: ("@" | "." | null)[][]): boolean => {

    let count = 0; 

    for (let row = 0; row < rolls.length; row++) {

        for (let col = 0; col < rolls[row].length; col++) {

            const accessRoll = rolls[row][col];

            if (col === 1 && row === 1) {
                continue;
            }

            if (accessRoll === ROLL) {
                count++;
            }
        }

    }

    if (count > NUMBER_OF_ROLLS_ACCESSIBLE) {
        return false;
    }

    return true;
}

export const getAdjacentRolls = (rowIndex: number, colIndex: number, rolls: ("@" | "." | null)[][]): ("@" | "." | null)[][] => {   
    return [
        [rolls[rowIndex - 1]?.[colIndex - 1] ?? null, rolls[rowIndex - 1]?.[colIndex] ?? null, rolls[rowIndex - 1]?.[colIndex + 1] ?? null],
        [rolls[rowIndex]?.[colIndex - 1] ?? null, rolls[rowIndex]?.[colIndex] ?? null, rolls[rowIndex]?.[colIndex + 1] ?? null],
        [rolls[rowIndex + 1]?.[colIndex - 1] ?? null, rolls[rowIndex + 1]?.[colIndex] ?? null, rolls[rowIndex + 1]?.[colIndex + 1] ?? null],
    ];
}

const rollsMatrix = buildRollsMatrix(sampleRolls);


// find the two maximum values in the array
rollsMatrix.forEach((row, rowIndex) => {

    // iterate through each character in the row
    for (let col = 0; col < row.length; col++) {

        const pointer = row[col];

        console.log(`Checking roll at row ${rowIndex} and col ${col}: ${pointer}`);

        if (pointer !== ROLL) {
            continue;
        }
        
        const adjacentRolls = getAdjacentRolls(rowIndex, col, rollsMatrix );
        const isRollSafe = calculateIfRollIsSafe(adjacentRolls);
        
        if (isRollSafe) {
            accessibleRolls++;
        }

    }
});

